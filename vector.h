class Vector {
        public:
                float x,y,z;
                float Length();
                void Normal();
};
//===========================================================================
float Vector::Length() {
        return sqrt(x*x+y*y+z*z);
}
//===========================================================================
void Vector::Normal() {
        float l=Length();
        if(l==0)
                return;
        x/=l;
        y/=l;
        z/=l;
}
